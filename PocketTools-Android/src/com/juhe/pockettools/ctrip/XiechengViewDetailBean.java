package com.juhe.pockettools.ctrip;


public class XiechengViewDetailBean{

	private String ticket_kind;
	private String market_price;
	private String price;
	private String advanceBookingTime;

	public XiechengViewDetailBean(){
	}
	
	public String getTicket_kind() {
		return ticket_kind;
	}

	public void setTicket_kind(String ticket_kind) {
		this.ticket_kind = ticket_kind;
	}

	public String getMarket_price() {
		return market_price;
	}

	public void setMarket_price(String market_price) {
		this.market_price = market_price;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getAdvanceBookingTime() {
		return advanceBookingTime;
	}

	public void setAdvanceBookingTime(String advanceBookingTime) {
		this.advanceBookingTime = advanceBookingTime;
	}


}
